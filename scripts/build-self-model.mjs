#!/usr/bin/env node
// build-self-model.mjs — 只读扫描 luca_gstack 演进面，生成 self-model.generated.yaml。
// 用法：
//   node scripts/build-self-model.mjs          # 重新生成 generated 文件
//   node scripts/build-self-model.mjs --check   # 只比对实时扫描 vs 现有 generated，漂移则 exit 1（不写盘）
// 红线：本脚本只写 self-model.generated.yaml；self-model.yaml 等人工文件一律不碰。

import fs from 'node:fs'
import path from 'node:path'
import os from 'node:os'
import { fileURLToPath } from 'node:url'

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const OUT = path.join(ROOT, '.claude/skill-os/evolution/self-model.generated.yaml')

// statSync follows symlinks — many global skills are symlinked, so Dirent.isDirectory() would miss them.
const isDirFollow = fp => { try { return fs.statSync(fp).isDirectory() } catch { return false } }
const isFileFollow = fp => { try { return fs.statSync(fp).isFile() } catch { return false } }
const listDirs = (p, exclude = []) => {
  try {
    return fs.readdirSync(p)
      .filter(name => !exclude.includes(name) && isDirFollow(path.join(p, name)))
      .sort()
  } catch { return [] }
}
const listFiles = (p, ext) => {
  try {
    return fs.readdirSync(p)
      .filter(name => name.endsWith(ext) && isFileFollow(path.join(p, name)))
      .map(name => name.slice(0, -ext.length)).sort()
  } catch { return [] }
}
const exists = rel => { try { fs.accessSync(path.join(ROOT, rel)); return true } catch { return false } }

const inventory = {
  skills_office: listDirs(path.join(ROOT, '.claude/skills/office'), ['references']),
  skills_global: listDirs(path.join(os.homedir(), '.claude/skills'), ['assets', 'references']),
  agents: listFiles(path.join(ROOT, '.claude/agents'), '.md'),
  hooks: listFiles(path.join(ROOT, '.claude/hooks'), '.mjs'),
}
const routingFiles = [
  '.claude/skill-os/skill-routing-map.yaml',
  '.claude/skill-os/input-modes.yaml',
  '.claude/skill-os/optional-workflow-graph.yaml',
  '.claude/observability/rules.yaml',
].filter(exists).map(f => f.split('/').pop())

const flow = arr => '[' + arr.join(', ') + ']'
const render = (genAt) => [
  '# AUTO-GENERATED by scripts/build-self-model.mjs — DO NOT EDIT BY HAND.',
  '# 重新生成：node scripts/build-self-model.mjs',
  'generated_at: ' + genAt,
  'counts:',
  '  skills_office: ' + inventory.skills_office.length,
  '  skills_global: ' + inventory.skills_global.length,
  '  agents: ' + inventory.agents.length,
  '  hooks: ' + inventory.hooks.length,
  'already_have_ondisk:',
  '  skills_office: ' + flow(inventory.skills_office),
  '  skills_global: ' + flow(inventory.skills_global),
  '  agents: ' + flow(inventory.agents),
  '  hooks: ' + flow(inventory.hooks),
  'routing_files_present: ' + flow(routingFiles),
  '',
].join('\n')

// --check：比对实时清单 vs 现有 generated（忽略 generated_at 时间戳），漂移则 exit 1
if (process.argv.includes('--check')) {
  let prev = ''
  try { prev = fs.readFileSync(OUT, 'utf8') } catch {}
  const strip = s => s.split('\n').filter(l => !l.startsWith('generated_at:')).join('\n')
  if (strip(prev) !== strip(render('IGNORED'))) {
    console.error('[build-self-model] DRIFT: self-model.generated.yaml 与实时扫描不一致，请运行 node scripts/build-self-model.mjs')
    process.exit(1)
  }
  console.log('[build-self-model] OK: generated 与实时一致')
  process.exit(0)
}

const genAt = new Date().toISOString().replace(/\.\d{3}Z$/, 'Z')
fs.writeFileSync(OUT, render(genAt))
console.log('[build-self-model] wrote ' + path.relative(ROOT, OUT))
console.log('  skills_office=' + inventory.skills_office.length +
  ' skills_global=' + inventory.skills_global.length +
  ' agents=' + inventory.agents.length + ' hooks=' + inventory.hooks.length)
